package com.sabo.monaksi.Model;

public class RapatModel {
    private int id_rapat, status;
    private String nama_rapat;

    public int getId_rapat() {
        return id_rapat;
    }

    public void setId_rapat(int id_rapat) {
        this.id_rapat = id_rapat;
    }

    public int getStatus() {
        return status;
    }

    public void setStatus(int status) {
        this.status = status;
    }

    public String getNama_rapat() {
        return nama_rapat;
    }

    public void setNama_rapat(String nama_rapat) {
        this.nama_rapat = nama_rapat;
    }
}
