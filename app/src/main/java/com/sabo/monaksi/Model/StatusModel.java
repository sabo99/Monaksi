package com.sabo.monaksi.Model;

public class StatusModel {
    private int revisi, belumdikerjakan, onprogress, selesai, approved, verified;

    public int getRevisi() {
        return revisi;
    }

    public void setRevisi(int revisi) {
        this.revisi = revisi;
    }

    public int getBelumdikerjakan() {
        return belumdikerjakan;
    }

    public void setBelumdikerjakan(int belumdikerjakan) {
        this.belumdikerjakan = belumdikerjakan;
    }

    public int getOnprogress() {
        return onprogress;
    }

    public void setOnprogress(int onprogress) {
        this.onprogress = onprogress;
    }

    public int getSelesai() {
        return selesai;
    }

    public void setSelesai(int selesai) {
        this.selesai = selesai;
    }

    public int getApproved() {
        return approved;
    }

    public void setApproved(int approved) {
        this.approved = approved;
    }

    public int getVerified() {
        return verified;
    }

    public void setVerified(int verified) {
        this.verified = verified;
    }
}
